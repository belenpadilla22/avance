
package inventario;


public class Usuario {
    String nombre,contra;
    String flores, rosas,tulipanes,girasoles,jazmin;

    public Usuario(String nombre, String contra) {
        this.nombre = nombre;
        this.contra = contra;
    }

    
    
    public String getRosas() {
        return rosas;
    }

    public void setRosas(String rosas) {
        this.rosas = rosas;
    }

    public String getTulipanes() {
        return tulipanes;
    }

    public void setTulipanes(String tulipanes) {
        this.tulipanes = tulipanes;
    }

    public String getGirasoles() {
        return girasoles;
    }

    public void setGirasoles(String girasoles) {
        this.girasoles = girasoles;
    }

    public String getJazmin() {
        return jazmin;
    }

    public void setJazmin(String jazmin) {
        this.jazmin = jazmin;
    }

    public String getFlores() {
        return flores;
    }

    public void setFlores(String flores) {
        this.flores = flores;
    }
    
    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getContra() {
        return contra;
    }

    public void setContra(String contra) {
        this.contra = contra;
    }

   
    
}
